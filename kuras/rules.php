<h3>The rules</h3>
<ul>
 <li>1. There is only one examination buklet for each student.</li>
 <li>2. Make sure that you do not have book, copybook or other informative thungs on table. Especially mobile phones.</li>
 <li>3.Do not copy, change your paper with other students. </li>
 <li>4. Make sure that you wrote your full name, teacher's name. Otherwise your work will not be accepted.</li>
 <li>5. You may go out after 15 minutes from the beginning of examination time.</li>
 <li>6. Be quiet until the end of time. </li>
</ul>
<h3>The tips</h3>
<ul>
<li>1. In order to be well prepared look learning objectives of subject that were during the term.</li>
<li>2. Ask your teacher to give you specific paper if she has. There you can find example questions.</li>
<li>3. Read all presentations that you had during the term. They can be found in our site. <li>
</ul>
